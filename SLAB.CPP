#include <iostream>
using namespace std;

int main() {
	
	int n;
	cin>>n;
    while(n>0)
    {
	n-=1;
	double s,tax=0;
	cin>>s;
	tax=0;
	if(s<=250000)
		tax=0;
	else if(s<=500000)
		tax=0.05*(s-250000);
	else if(s<=750000)
		tax=0.05*250000+0.1*(s-500000);
	else if(s<=1000000)
		tax=0.05*250000+0.1*250000+(s-750000)*0.15;
	else if(s<=1250000)
		tax=0.05*250000+0.1*250000+250000*0.15+(s-1000000)*0.2;
	else if(s<=1500000)
		tax=0.05*250000+0.1*250000+250000*0.15+250000*0.20+(s-1250000)*0.25	;
	else
		tax=0.05*250000+0.1*250000+250000*0.15+(250000)*0.2+250000*0.25+(s-1500000)*0.3;
	s=s-tax;
	cout<<((int)(s))<<"\n";
    }
	return 0;
}
